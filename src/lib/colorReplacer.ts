/**
 * Replace yellow color classes in a className string
 */
export const replaceYellowInClassName = (className: string): string => {
  let result = className;

  for (const [yellow, orange] of Object.entries(colorReplacements)) {
    // Use word boundaries to avoid partial matches
    const regex = new RegExp(
      `\\b${yellow.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}\\b`,
      "g",
    );
    result = result.replace(regex, orange);
  }

  return result;
};

